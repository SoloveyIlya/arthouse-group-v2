// Language switcher functionality
let currentLanguage = localStorage.getItem('language') || 'ru';

function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update active language button
  document.querySelectorAll('[data-lang]').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('text-primary');
      btn.classList.remove('text-gray-400');
    } else {
      btn.classList.remove('text-primary');
      btn.classList.add('text-gray-400');
    }
  });
  
  // Update product modal if it's open and has a current product
  if (window.currentProductId && document.getElementById('productModal').classList.contains('flex')) {
    if (window.updateProductModalContent) {
      window.updateProductModalContent(window.currentProductId);
    }
  }
  
  console.log('Language changed to:', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Set initial language
  setLanguage(currentLanguage);
  
  // Add click handlers to language buttons
  document.querySelectorAll('[data-lang]').forEach(button => {
    button.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
});

// Export for use in other scripts
window.setLanguage = setLanguage;
window.translations = translations;
window.getCurrentLanguage = () => currentLanguage;

